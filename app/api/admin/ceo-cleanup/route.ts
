import { NextResponse } from "next/server";
import { createClient } from "@supabase/supabase-js";

// Initialize admin client
const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL || "https://placeholder.supabase.co";
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_SERVICE_KEY || "";

export async function POST() {
    try {
        if (!supabaseUrl || !supabaseServiceKey) {
            return NextResponse.json(
                { error: "Missing Supabase configuration" },
                { status: 500 }
            );
        }

        const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey);

        const now = new Date();
        const twentyFourHoursAgo = new Date(now.getTime() - 24 * 60 * 60 * 1000).toISOString();

        // Delete requests that were reviewed more than 24 hours ago
        const { data: deletedRequests, error: requestsError } = await supabaseAdmin
            .from("requests")
            .delete()
            .neq("status", "pending")
            .lt("reviewed_at", twentyFourHoursAgo);

        if (requestsError) {
            console.error("Error cleaning up requests:", requestsError);
        }

        // Delete completed tasks older than 24 hours
        const { data: deletedTasks, error: tasksError } = await supabaseAdmin
            .from("tasks")
            .delete()
            .eq("status", "completed")
            .lt("updated_at", twentyFourHoursAgo);

        if (tasksError) {
            console.error("Error cleaning up tasks:", tasksError);
        }

        // Delete expired notifications
        const { data: deletedNotifications, error: notificationsError } = await supabaseAdmin
            .from("notifications")
            .delete()
            .lt("expires_at", now.toISOString())
            .neq("expires_at", null);

        if (notificationsError) {
            console.error("Error cleaning up notifications:", notificationsError);
        }

        return NextResponse.json({
            success: true,
            message: "Cleanup completed",
            deleted: {
                requests: deletedRequests?.length || 0,
                tasks: deletedTasks?.length || 0,
                notifications: deletedNotifications?.length || 0
            },
            timestamp: now.toISOString()
        });
    } catch (error: any) {
        console.error("Cleanup error:", error);
        return NextResponse.json(
            { error: error.message || "Internal server error" },
            { status: 500 }
        );
    }
}

// Also handle GET for manual triggering
export async function GET() {
    return POST();
}

