-- First, rename 'description' to 'content' if it exists
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = 'ideas' AND column_name = 'description'
    ) AND NOT EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = 'ideas' AND column_name = 'content'
    ) THEN
        ALTER TABLE ideas RENAME COLUMN description TO content;
    END IF;
END $$;

-- Add content column if neither description nor content exists
