   OR archived IS NULL 
   OR shared_with IS NULL 
   OR tags IS NULL;

-- =====================================================
-- Optional: Create a view for due reminders
-- =====================================================
CREATE OR REPLACE VIEW due_reminders AS
SELECT 
    id,
    title,
    content,
    status,
    tags,
    follow_up_date,
    auto_tagged,
    created_by,
    shared_with,
    archived,
    created_at,
    CASE 
        WHEN follow_up_date < CURRENT_DATE THEN 'overdue'
        WHEN follow_up_date = CURRENT_DATE THEN 'today'
        WHEN follow_up_date = CURRENT_DATE + INTERVAL '1 day' THEN 'tomorrow'
        ELSE 'future'
    END as urgency
FROM ideas
WHERE follow_up_date IS NOT NULL
  AND archived = FALSE
  AND follow_up_date <= CURRENT_DATE + INTERVAL '1 day';

